var paletteSin = []color.Color{color.Black, color.RGBA{0x40, 0xAD, 0x3A, 0xff}}

//SinGif draw a sin
func SinGif(out io.Writer) {
	const (
		cycles  = 5
		res     = 0.001
		size    = 100
		nframes = 64
		delay   = 8
	)
	//freq := rand.Float64() * 3.0
	anim := gif.GIF{LoopCount: nframes}
	phase := 0.0
	for i := 0; i < nframes; i++ {
		rect := image.Rect(0, 0, 2*size+1, 2*size+1)
		img := image.NewPaletted(rect, paletteSin)
		for t := 0.0; t < cycles*2*math.Pi; t += res {
			x := t
			y := math.Sin(t + phase)
			img.SetColorIndex(int(x*2*size/(cycles*2*math.Pi)), size+int(y*size+0.5), greenIndex)
		}
		phase += 0.1
		anim.Delay = append(anim.Delay, delay)
		anim.Image = append(anim.Image, img)
	}
	gif.EncodeAll(out, &anim)
}
